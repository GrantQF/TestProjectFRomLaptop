package class7;

public class ForDemo {
    public static void main(String[] args) {
        int number = 0;
        while (number < 10) {
            System.out.println(number);
            number++;
        }
        System.out.println("*******************************");
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }
    }
}
