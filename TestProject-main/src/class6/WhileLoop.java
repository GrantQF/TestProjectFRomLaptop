package class6;

public class WhileLoop {
    public static void main(String[] args) {
        System.out.println("Hello World");
        int number=1;
        while (number<5){
            System.out.println(number);
            number++;
        }
        System.out.println("****************");
        int number2=0;
        while (number2<10){
            System.out.println(number2);
            number2+=2;
        }
        System.out.println("******************");
        int number3=1;
        while (number3<101){
            System.out.println(number3);
            number3++;
        }

    }

}
